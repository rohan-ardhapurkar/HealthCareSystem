package pojo;

public class PatientPojo {
    private String pName;
    private String pEmail;
    private String pContact;
    private String pAddress;
    private String pDescription;
    private String pEnquiryDate;

    /**
     * @return the pName
     */
    public String getpName() {
        return pName;
    }

    /**
     * @param pName the pName to set
     */
    public void setpName(String pName) {
        this.pName = pName;
    }

    /**
     * @return the pEmail
     */
    public String getpEmail() {
        return pEmail;
    }

    /**
     * @param pEmail the pEmail to set
     */
    public void setpEmail(String pEmail) {
        this.pEmail = pEmail;
    }

    /**
     * @return the pContact
     */
    public String getpContact() {
        return pContact;
    }

    /**
     * @param pContact the pContact to set
     */
    public void setpContact(String pContact) {
        this.pContact = pContact;
    }

    /**
     * @return the pAddress
     */
    public String getpAddress() {
        return pAddress;
    }

    /**
     * @param pAddress the pAddress to set
     */
    public void setpAddress(String pAddress) {
        this.pAddress = pAddress;
    }

    /**
     * @return the pDescription
     */
    public String getpDescription() {
        return pDescription;
    }

    /**
     * @param pDescription the pDescription to set
     */
    public void setpDescription(String pDescription) {
        this.pDescription = pDescription;
    }

    /**
     * @return the pEnquiryDate
     */
    public String getpEnquiryDate() {
        return pEnquiryDate;
    }

    /**
     * @param pEnquiryDate the pEnquiryDate to set
     */
    public void setpEnquiryDate(String pEnquiryDate) {
        this.pEnquiryDate = pEnquiryDate;
    }
    
}
